# TUPLES FUNCTIONS

tup1=(2 , 4 , 1 , 3 , 1)

# tup.index(element) : Returns index of first occurence of element
print(tup1.index(1)) #Since element "1" came on index 2 for the first time so it wil return 2

# tup.count(element) : Counts total occurances of the element in the tuple
print(tup1.count(1)) #Since 1 comes 2 times in tuple so it will return 2