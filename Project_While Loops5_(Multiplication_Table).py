# MULTIPLICATION TABLE : WAP to print the multiplication table of a number n

n=int(input("Enter a number: "))
i = 1
while i<=10 : 
    print(i*n)
    i += 1  

# Concept Used :
 
#  let n=3
#  table of n is : 3  :  3 x 1
#                  6  :  3 x 2
#                  9  :  3 x 3
#                 12  :  3 x 4
#  and so on .....

# here 3 is a constant and 1 , 2 , 3 etc are the variable 
# so i stores the variable and the stopping condition is 10 as we need a table till multiple of 10 

