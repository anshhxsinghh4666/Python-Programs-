# EASCAPE SEQUENCE CHARACTERS (\n , \t , etc....)

str1= "This is a string. We are creating it in python"
print (str1)

# \n changes the paragrapgh. So output will be : "This is a string. 
#                                                 We are creating it in python"
str2="This is a string. \nWe are creating it in python"
print(str2)

# \t adds a tab space btween two lines. So output will be: "This is a string.   We are creating it in python"
str3="This is a string. \tWe are creating it in python"
print(str3)